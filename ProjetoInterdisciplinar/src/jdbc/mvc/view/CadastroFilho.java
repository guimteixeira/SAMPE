private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        
        int idade=0;
        try
        {
            idade = Integer.parseInt(jTextField2.getText());
        }
        catch(NumberFormatException e)
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Idade Inv√°lida","Cadastro de Filho",JOptionPane.ERROR_MESSAGE);
            return;
        }
        Filho_model f = new Filho_model();
        f.setNome(jTextField1.getText());
        f.setIdade(idade);
        
        
        try
        {
            Filho_CRUD fc = new Filho_CRUD();
            fc.inserir(f);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField1.requestFocus();

        }//try
        catch(Exception exc)
        {
            exc.getMessage();
        }
        
        try {
        Filho_CRUD fc = new Filho_CRUD();
        List<Filho_model> lista = (List<Filho_model>) fc.busca();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setRowCount(0); 

        for (int i=0;i<lista.size();i++)
        {
             modelo.addRow(new Object[]{
                                lista.get(i).getNome(),
                                lista.get(i).getIdade()
                          });
        }
      } catch (Exception exc) {
            exc.getMessage();
      }
}//GEN-LAST:event_jButton3ActionPerformed

